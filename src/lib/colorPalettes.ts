export function getPalette(id: string | null | undefined): ColorPalette {
  return COLOR_PALETTES.find(p => p.id === id) ?? COLOR_PALETTES[0];
}

export function buildPaletteCSS(palette: ColorPalette): string {
  if (palette.id === 'default') return '';

  const lightVars = Object.entries(palette.light)
    .map(([k, v]) => `  --${k}: ${v};`)
    .join('\n');
  const darkVars = Object.entries(palette.dark)
    .map(([k, v]) => `  --${k}: ${v};`)
    .join('\n');

  return `:root {\n${lightVars}\n}\n.dark {\n${darkVars}\n}`;
}
